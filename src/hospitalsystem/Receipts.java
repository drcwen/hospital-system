    private void receiptNumber_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_receiptNumber_buttonActionPerformed
        String SUrl = "jdbc:mysql://localhost:3306/hospitalsystem";
        String SUser = "root";
        String SPass = "";

        String keyword = receiptNumber_search.getText();

        String query = "SELECT * FROM receipt\n" +
        "WHERE receipt_number = " + keyword + "";

        try (Connection con = DriverManager.getConnection(SUrl, SUser, SPass)) {
            Statement st = con.createStatement();

            ResultSet rs = st.executeQuery(query);

            while (rs.next()) {
                String receipt_id = rs.getString("receipt_number");
                jLabel10.setText(receipt_id);

                String id = rs.getString("patient_id");
                jLabel16.setText(id);

                String firstname = rs.getString("patient_firstName");
                jLabel14.setText(firstname);

                String lastname = rs.getString("patient_lastName");
                jLabel12.setText(lastname);

                String price = rs.getString("payment_price");
                jLabel32.setText(price);

                String balance = rs.getString("balance");
                jLabel33.setText(balance);
                
                String dateOfPayment = rs.getString("dateOfPayment");
                jLabel8.setText(dateOfPayment);
            }

            String patientID = jLabel16.getText();

            String query2 = "SELECT * FROM finalizebills\n" +
            "WHERE patient_id = " + patientID + "";

            Statement st2 = con.createStatement();
            ResultSet rs2 = st2.executeQuery(query2);

            while (rs2.next()) {
                String roomTotal = rs2.getString("roomcharge_total");
                jLabel26.setText(roomTotal);

                String physicianTotal = rs2.getString("physicianfee_total");
                jLabel27.setText(physicianTotal);

                String labTotal = rs2.getString("labtest_total");
                jLabel28.setText(labTotal);

                String surgeryTotal = rs2.getString("surgery_total");
                jLabel29.setText(surgeryTotal);

                String nursingTotal = rs2.getString("nursing_total"); 
                jLabel30.setText(nursingTotal);

                String finalTotal = rs2.getString("final_total");
                jLabel31.setText(finalTotal);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_receiptNumber_buttonActionPerformed

