    private void date_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_date_searchActionPerformed
        
       String selected = date_combo.getSelectedItem().toString(); // Assumes format matches the database's DATETIME format

    String SUrl = "jdbc:mysql://localhost:3306/hospitalsystem";
    String SUser = "root";
    String SPass = "";

    String sql = "SELECT * FROM patientarchive WHERE date_of_action = ?";

    try (Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
         PreparedStatement pst = con.prepareStatement(sql)) {

        // Set the parameter for the query
        pst.setString(1, selected);

        try (ResultSet rs = pst.executeQuery()) {
            while (rs.next()) {
                // Retrieve data from result set
                String name = rs.getString("patient_firstName");
                String lastname = rs.getString("patient_lastName");
                String age = rs.getString("patient_age");
                String bday = rs.getString("patient_birthday");
                String contact = rs.getString("patient_contact");
                String allergies = rs.getString("patient_allergies");
                String address = rs.getString("patient_address");
                String ailment = rs.getString("patient_ailment");
                String type = rs.getString("patient_type");
                String bloodtype = rs.getString("blood_type");
                String chronic = rs.getString("chronic_conditions");
                String person_emergency = rs.getString("person_emergency");
                String contact_emergency = rs.getString("contact_emergency");
                String physician = rs.getString("physician");
                String room_number = rs.getString("room_number");
                String admission_date = rs.getString("admission_date");
                String admission_time = rs.getString("admission_time");
                String action = rs.getString("action");

                // Update GUI components
                pati_firstName17.setText(name);
                pati_lastName.setText(lastname);
                pati_age.setText(age);
                pati_birthday.setText(bday);
                pati_contact.setText(contact);
                pati_allergies.setText(allergies);
                pati_address.setText(address);
                pati_ailment.setText(ailment);
                pati_type.setText(type);
                pati_bloodType.setText(bloodtype);
                pati_chronicConditions.setText(chronic);
                pati_personEmergency.setText(person_emergency);
                pati_contactEmergency.setText(contact_emergency);
                pati_physician.setText(physician);
                pati_roomNum.setText(room_number);
                pati_admissionDate.setText(admission_date);
                pati_admissionTime.setText(admission_time);
                action_label.setText(action);
            }
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
       
    }//GEN-LAST:event_date_searchActionPerformed

