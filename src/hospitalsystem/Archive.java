    private void searchPatientIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchPatientIDActionPerformed
        
        String id = patientID_search.getText();
        
        String SUrl = "jdbc:mysql://localhost:3306/hospitalsystem";
        String SUser = "root";
        String SPass = "";
        
        try {
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st = con.createStatement();
            String sql = "SELECT date_of_action from patientarchive where patient_id = " + id + "";

            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()) {
                String dates = rs.getString("date_of_action");
                date_combo.addItem(dates);
            }

        } catch (Exception e) {

        }

    }//GEN-LAST:event_searchPatientIDActionPerformed

