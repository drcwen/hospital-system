    private void name_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_name_searchActionPerformed
        String SUrl = "jdbc:mysql://localhost:3306/hospitalsystem";
        String SUser = "root";
        String SPass = "";
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        String keyword = name_finder.getText();

        String query = "SELECT * FROM receipt\n" +
        "WHERE patient_id LIKE '%" + keyword + "%' \n" +
        "   OR patient_firstName LIKE '%" + keyword + "%' \n" +
        "   OR patient_lastName LIKE '%" + keyword + "%' \n";
        
        try {
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st = con.createStatement();

            ResultSet rs = st.executeQuery(query);

            model.setRowCount(0);
            while (rs.next()) {

                String receipt_id = rs.getString("receipt_number");
                String id = rs.getString("patient_id");
                String firstname = rs.getString("patient_firstName");
                String lastname = rs.getString("patient_lastName");
                String price = rs.getString("payment_price");
                String balance = rs.getString("balance");

                String tbData[] = {receipt_id, id, firstname, lastname, price, balance};

                model.addRow(tbData);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
                                 
    }//GEN-LAST:event_name_searchActionPerformed

